package com.board.cha.bean;

public class BoardFile {
	private int bf_num;
	private int bf_bnum;
	private String bf_oriname;
	private String bf_sysname;
	
	public int getBf_num() {
		return bf_num;
	}
	public void setBf_num(int bf_num) {
		this.bf_num = bf_num;
	}
	public int getBf_bnum() {
		return bf_bnum;
	}
	public void setBf_bnum(int bf_bnum) {
		this.bf_bnum = bf_bnum;
	}
	public String getBf_oriname() {
		return bf_oriname;
	}
	public void setBf_oriname(String bf_oriname) {
		this.bf_oriname = bf_oriname;
	}
	public String getBf_sysname() {
		return bf_sysname;
	}
	public void setBf_sysname(String bf_sysname) {
		this.bf_sysname = bf_sysname;
	}
}
